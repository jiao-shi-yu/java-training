package wrapper;

public class WrapperDemo {
	public static void main(String[] args) {
		/**
		 * 基本类型
		 */
		byte bt = 3;
		short s = 7;
		int i = 1;
		long l = 5;
		float f = 4.1f;
		double d = 3.14;
		boolean b = true;
		char ch = 'A';

		/**
		 * 包装类
		 */		
		Byte bt1 = 3;
		Short sh1 = 7;
		Integer i1 = 1;
		Long l1 = 8l;
		Float f1 = 7f;
		Double d1 = 3.14;
		Boolean bool = true;
		Character chr = 'A';
	}
}
